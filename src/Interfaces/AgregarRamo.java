    private void home2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_home2ActionPerformed

        if (cont == 0) {
            int resp = JOptionPane.showConfirmDialog(null,
                    "Los Datos no guardado se eliminarán.\n ¿Desea ir al Menú principal igualmente?",
                    "Volver Menu Principal", JOptionPane.YES_NO_OPTION);
            if (JOptionPane.OK_OPTION == resp) {
                this.dispose();
                PaginaPrincipal pag = new PaginaPrincipal();
                pag.setTitle("PAGINA PRINCIPAL");
                pag.setVisible(true);
            }
        } else {

            this.dispose();
            PaginaPrincipal pag = new PaginaPrincipal();
            pag.setTitle("PAGINA PRINCIPAL");
            pag.setVisible(true);
            cont = 0;
        }
    }//GEN-LAST:event_home2ActionPerformed

