    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed

        if (!txtNombre.getText().equals("") && !txtPondTeorico.getText().equals("") && !txtPondPractico.getText().equals("") && !txtEvTeoricas.getText().equals("")
                && !txtEvPracticas.getText().equals("") && !txtHoras.getText().equals("") && !txtNivel.getText().equals("") && !modoAprob.getSelectedItem().equals(0)) {
            if (verificarRamo(txtNombre.getText())) {

                JOptionPane.showMessageDialog(this, "La asignatura ya se encuentra registrada");

            } else {
                if ((Double.parseDouble(txtPondTeorico.getText()) + Double.parseDouble(txtPondPractico.getText())) == 100) {

                    if (txtPondTeorico.getText().equals("100")) {
                        da.setTipo("Teórico");
                        op.agregarAsign(txtNombre.getText(), da.getTipo(), Integer.parseInt(txtEvTeoricas.getText()), Integer.parseInt(txtEvPracticas.getText()), Double.parseDouble(txtPondTeorico.getText()), Double.parseDouble(txtPondPractico.getText()), Integer.parseInt(txtHoras.getText()), Integer.parseInt(txtNivel.getText()));
                        JOptionPane.showMessageDialog(this, "Asignatura registrada!");
                        txtNombre.setEditable(false);
                        txtPondTeorico.setEditable(false);
                        txtHoras.setEditable(false);
                        txtEvTeoricas.setEditable(false);
                        txtEvPracticas.setEditable(false);
                        txtNivel.setEditable(false);
                        modoAprob.setEnabled(false);
                        cont++;
                    }else if (txtPondTeorico.getText().equals("0")) {
                        da.setTipo("Práctico");
                        op.agregarAsign(txtNombre.getText(), da.getTipo(), Integer.parseInt(txtEvTeoricas.getText()), Integer.parseInt(txtEvPracticas.getText()), Double.parseDouble(txtPondTeorico.getText()), Double.parseDouble(txtPondPractico.getText()), Integer.parseInt(txtHoras.getText()), Integer.parseInt(txtNivel.getText()));
                        JOptionPane.showMessageDialog(this, "Asignatura registrada!");
                        txtNombre.setEditable(false);
                        txtPondTeorico.setEditable(false);
                        txtHoras.setEditable(false);
                        txtEvTeoricas.setEditable(false);
                        txtEvPracticas.setEditable(false);
                        txtNivel.setEditable(false);
                        modoAprob.setEnabled(false);
                        cont++;
                    } 
                    if (!txtPondTeorico.getText().equals("0") && !txtPondTeorico.getText().equals("100")){
                        op.agregarAsign(txtNombre.getText(), modoAprob.getSelectedItem().toString(), Integer.parseInt(txtEvTeoricas.getText()), Integer.parseInt(txtEvPracticas.getText()), Double.parseDouble(txtPondTeorico.getText()), Double.parseDouble(txtPondPractico.getText()), Integer.parseInt(txtHoras.getText()), Integer.parseInt(txtNivel.getText()));
                        JOptionPane.showMessageDialog(this, "Asignatura registrada!");
                        txtNombre.setEditable(false);
                        txtPondTeorico.setEditable(false);
                        txtHoras.setEditable(false);
                        txtEvTeoricas.setEditable(false);
                        txtEvPracticas.setEditable(false);
                        txtNivel.setEditable(false);
                        modoAprob.setEnabled(false);
                        cont++;
                    }
                    
                } else if ((Double.parseDouble(txtPondTeorico.getText()) + Double.parseDouble(txtPondPractico.getText())) != 100) {
                    JOptionPane.showMessageDialog(null, "Ponderación no suma 100%",
                            "ERROR", JOptionPane.WARNING_MESSAGE);
                }
            }

        } else {
            JOptionPane.showMessageDialog(null, "Por favor rellene todos los campos",
                    "ERROR", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_jButton4ActionPerformed

