    private void txtPondTeoricoKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtPondTeoricoKeyTyped

        //DecimalFormat formatter = new DecimalFormat("#.##");  PARA PONER SOLO 1 DECIMAL 
        char c = evt.getKeyChar();

        if ((c < '0' || c > '9') && c != KeyEvent.VK_PERIOD) {
            evt.consume();
        }

        if ((c == KeyEvent.VK_PERIOD) && txtPondTeorico.getText().contains(".")) {
            evt.consume();
        }
        if (txtPondTeorico.getText().length() >= 4) {
            evt.consume();
        }
    }//GEN-LAST:event_txtPondTeoricoKeyTyped

    private void txtPondTeoricoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtPondTeoricoActionPerformed

        if (!txtPondTeorico.getText().equals("") && txtPondTeorico.getText().charAt(0) != '.') {
            if (Double.valueOf(txtPondTeorico.getText()) <= 100) {
                double pondP = 100 - Double.parseDouble(txtPondTeorico.getText());
                txtPondPractico.setText(String.valueOf(pondP));

                txtEvPracticas.setText("");
                txtEvPracticas.setEditable(true);
                txtEvTeoricas.setText("");
                txtEvTeoricas.setEditable(true);
            }
        }
        if (!txtPondTeorico.getText().equals("") && txtPondTeorico.getText().charAt(0) != '.') {
            if (Double.valueOf(txtPondTeorico.getText()) > 100) {
                jLabel13.setText("Introduce Ponderacion menor a 100");

            } else if (Double.valueOf(txtPondTeorico.getText()) == 100) {
                txtEvPracticas.setText("0");
                txtEvPracticas.setEditable(false);
            } else if (Double.valueOf(txtPondPractico.getText()) == 100) {
                txtEvTeoricas.setText("0");
                txtEvTeoricas.setEditable(false);
            }
        }
    }//GEN-LAST:event_txtPondTeoricoActionPerformed

